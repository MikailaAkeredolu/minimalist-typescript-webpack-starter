export class Item {
    
    id: number;
    name: string;
    price: number;
    discount: number;
    count: number

    constructor(id: number, name: string, price: number, discount: number, count: number) {
        this.id = id;
        this.name = name;
        this.price = price;
        this.discount = discount;
        this.count = count;
    }

}





